class Numero:

    def verifica_numero(self, numero):

        if numero == 1:
            return "UM"

        elif numero == 2:
            return "DOIS"

        elif numero == 3:
            return "TRÊS"

        elif numero == 4:
            return "QUATRO"

        elif numero == 5:
            return "CINCO"

        elif numero == 6:
            return "SEIS"

        elif numero == 7:
            return "SETE"

        else:
            print("Número Inválido")